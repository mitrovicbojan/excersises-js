function telephoneCheck(str) {
  let myReg = new RegExp(
    "^[1]{1} [5-9]{0,2}[2-9]{1}[0-9]{2} [2-9]{1}[0-9]{2} [0-9]{4}$|^[5-9]{0,2}[2-9]{1}[0-9]{2}[2-9]{1}[0-9]{2}[0-9]{4}$|^[5-9]{0,2}[2-9]{1}[0-9]{2} [2-9]{1}[0-9]{2} [0-9]{4}$|^[0-9]{0,2}[2-9]{1}[0-9]{2}-[2-9]{1}[0-9]{2}-[0-9]{4}$|^\\([0-9]{0,2}[2-9]{1}[0-9]{2}\\)[2-9]{1}[0-9]{2}-[0-9]{4}$|^\\([0-9]{0,2}[2-9]{1}[0-9]{2}\\) [2-9]{1}[0-9]{2}-[0-9]{4}$|^[1]{1}[0-9]{0,2}[2-9]{1}[0-9]{2}[2-9]{1}[0-9]{2}[0-9]{4}$|^[1]{1} [5-9]{0,2}[2-9]{1}[0-9]{2}-[2-9]{1}[0-9]{2}-[0-9]{4}$|^[1]{1} \\([5-9]{0,2}[2-9]{1}[0-9]{2}\\) [2-9]{1}[0-9]{2}-[0-9]{4}$|^[1]{1}\\([5-9]{0,2}[2-9]{1}[0-9]{2}\\)[2-9]{1}[0-9]{2}-[0-9]{4}$"
  );
  if (myReg.test(str)) {
    console.log("Phone number is valid");
    return true;
  } else {
    console.log("Phone number is not valid");
    return false;
  }
}

